<p style="font-size:120%;color:#0E7EC0">
Why is Life Cover important for you?
</p>
<p style="font-size:120%">
Life Cover can provide an early payment of your benefit, if you are diagnosed as being terminally ill and likely to die within 12 months.  This means you can spend your remaining time with your three children and loved ones without worrying about your finances.
</p>
<p style="font-size:120%">
Term Life insurances pays a lump sum of money if you pass away or become terminally ill.  Term Life Insurance is designed to help your family pay costs that you would have helped with if you were still alive.  Life Insurance is to ensure the future of your loved ones will be financially secure in the event of your untimely death.  The Life cover quoted will enable a surviving spouse to lead the life you want without financial restraint.
</p>

<p style="font-size:120%;color:#0E7EC0">
Why do you require this level of cover?
</p>
<p style="font-size:120%">
Life Cover will ensure that in the event of your untimely death a lump sum payment will be available. This level of cover currently meets your financial commitments, leaving no debts, liabilities or burdens on family and friends in the event of your untimely death.
</p>
<p style="font-size:120%">
My recommendations for the sum assured is based on future income for your surviving partner and family, clearing of the remaining debts, future education costs for your children and funeral costs.
</p>

<p style="font-size:120%;color:#0E7EC0">
Why is this benefit and premium structure important?
</p>
<p style="font-size:120%">
I have recommended that your Life Cover Benefit is increased annually in line with the Consumer Price Index so your sum assured keeps up with inflation, your premiums will also increase with inflation.
</p>
<p style="font-size:120%">
The rate for age premium structure means that your premium will increase annually in line with your age and in line with the sum assured. This option ensures that your premium increases are smoother over time rather than facing a potentially large premium increase at the end of a level premium term.
</p>

<p style="font-size:120%;color:#0E7EC0">
What are the Benefits and Limitations of this Benefit?
</p>
<p style="font-size:120%">
<b>Terminal Illness</b> - If you are diagnosed with a terminal Illness and will die within 12 months of the diagnosis, you can elect to have some or all of your Life Cover paid in advance. The Life Cover benefit is reduced by the amount of any Terminal Illness benefit paid.
</p>

