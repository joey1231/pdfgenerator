<div style="font-size: 110%;">
  <h3>What you need to tell us</h3>
  <ol>
    <li>Always tell the truth. Insurance is based on the principle of utmost good faith. Put simply youhave a positive duty to provide the insurance provider with all information that a prudent insurer would want to take into account in deciding whether to insure you, and if so, on what terms. The information you do provide must be truthful, complete and correct information
       about yourself, including your health and medical history. This is called your duty of
       disclosure. Your duty of disclosure extends to the date the contract of insurance is concluded
       between yourself and the insurance provider. For example, you are required to tell the
       insurance provider if you are diagnosed with a medical condition after the date of your
       application but before you agree to any terms of cover they may offer. If they offer to cover
       you, you will be insured on the basis of the information you have provided.
       <br>
    </li>
    <li>Answer questions as fully as you can. Applying for insurance involves responding to a number
       of questions. Your answers need to include as much detail relating to your current and past
       circumstances as possible. While this may take time, it is important to ensure that the
       insurance provider has all the information they need when they make the decision to insure
       you and on what terms.<br>
    </li>
    <li>If in doubt, tell the insurance provider. If you are uncertain of the relevance of any
      information, our advice is to include it on your form because, even if you aren't sure, it may
      be important to the insurance provider. If someone else is completing the form on your behalf,
      it is important that you check that the information is correct and nothing has been left out.<br>
    </li>

    <li>If you don't know something, say so. If you say that you don't know what the answer to a
       question is and the insurance provider thinks they need more information about your answer
       to a question so they can offer you insurance, they will need to obtain the information from
       somewhere else. By signing the declaration and consent, you give them your consent to get
       this information.<br>
    </li>
    <li>Know what you're signing. By signing the declaration on your form, you are saying that you
       have answered all the questions completely and to the best of your knowledge, as well as
       providing any other information that may influence the insurance provider's decision about
       your policy. If you are uncertain about any of your answers, ask the insurance provider or your
       adviser before signing the declaration.<br>
    </li>
    <li>How non-disclosure affects claims. When you make a claim the insurance provider may look
       further into your personal history. If they discover that you did not comply with your duty of
       disclosure, they may either alter the terms of your policy retrospectively which might affect
       your claim, or they may avoid your policy from its inception, which means that you would not
       be able to make a claim as no policy would exist. It does not matter if the material
       information is about a condition unrelated to your claim.<br>
    </li>
    <li>Help the insurance provider to help you when you need to claim. Depending on what you tell
       them on your claim form, they might need more information to make a decision about your
       claim. They may get this information by calling you, asking you to fill out another form or
       asking you to take a medical test. Sometimes the insurance provider will need to get
       information from other people who may include your doctor, your employer, ACC or other
       government departments. By signing the claim form you give them your consent to do this.<br>
    </li>
    <li>Know what you are consenting to. The insurance provider can only request information that
       they need to assess your application for insurance or for payment of a claim. At all times, the
       information they hold about you is your information, you have the right to access and, if it is
       wrong, to ask the insurance provider to correct it.<br>
    </li>
    <li>Don't be afraid to ask. If there is anything you're not sure of, don't be afraid to ask for help.<br>
    </li>
  </ol>
</div>

