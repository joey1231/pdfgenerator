  <h2>What you need to know from us</h2><br />
    <h3>Benefit types</h3>
    <p>Products are generally defined as being 'benefits' in Insurance terms.
      Below are definitions of common types of benefits.</p>
    <h3>Health insurance</h3>
    <p>Health insurance is designed to protect you and your dependants against any financial constraints arising
  due to hospital care or an operation.</p>
    <h3>Income protection</h3>
    <p>Income protection can provide you with a monthly payment if you are unable to work due to sickness or
  injury. There are two types of income protection: indemnity and agreed value.</p>
    <h3>&nbsp;&nbsp;&nbsp;&nbsp;Indemnity</h3>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;Provides a monthly benefit up to your insured amount or <br>&nbsp;&nbsp;&nbsp;&nbsp;75% of your earnings prior to disablement
  (whichever is the lesser).</p>
    <h3>&nbsp;&nbsp;&nbsp;&nbsp;Agreed value</h3>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;Provides a monthly benefit for a percentage of your gross <br>&nbsp;&nbsp;&nbsp;&nbsp; remuneration package, agreed at the time
  you take out your policy.</p>
    <h3>Waiver of Premium</h3>
    <p>Waiver of Premium is designed to take care of your policy premium payments while you are on claim
  due to sickness or injury.</p>
    <h3>Trauma (living assurance)</h3>
    <p>Trauma insurance (living assurance) is designed to pay a lump sum to you if you suffer a defined critical
  illness, to help you cope with its impact on your lifestyle, family and work.</p>
    <h3>Total permanent disablement</h3>
    <p>Total permanent disablement insurance pays you a lump sum if you suffer an illness or injury that
  prevents you from ever working again. To make a claim, you would need to be off work for a minimum
  period of time, generally three months and be deemed to be totally and permanently disabled.</p>
    <h3>Life cover</h3>
    <p>Life cover is a lump sum paid upon the death of the insured, or the diagnosis of a terminal illness in
  accordance with the policy conditions.</p>
    <h3>Accidental death</h3>
    <p>Provides a lump sum should you die as a result of accidental injury.</p>
    <h3>Family protection</h3>
    <p>Family protection is a monthly benefit paid upon the death of the insured, or the diagnosis of a terminal
  illness in accordance with the policy conditions.</p>
    <h3>Optional benefits</h3>
    <p>These are additional benefits that can be added to a policy in exchange for additional
  premium.</p>
    <h3>Benefit terms</h3>
    <p>The benefit term is the maximum period for which the benefit is payable, as stated in the policy
  documents. This will be the duration of the sickness or injury or the time stated in your benefit details,
  whichever is the earlier. This timeframe could be a set time or to a set age depending on the product.</p>
    <h3>Waiting periods</h3>
    <p>The waiting period is the time you must wait before a benefit is paid. You choose the waiting period in
  consultation with your adviser. Wait periods vary from two weeks to two years, with multiple options in
  between.</p>
    <h3>Benefit amount/Sum assured</h3>
    <p>The benefit amount is the level of cover that you have on a particular product. This may show as a lump
  sum amount or as a monthly amount depending on the benefit type.</p>
    <h3>Benefit indexation</h3>
    <p>Some benefits have an indexation option which allows the benefit amount/sum assured to increase
  annually in line with inflation. When the benefit amount/sum assured increases, there is a
  corresponding increase in premium.</p>
    <h3>Standalone vs Accelerated</h3>
    <p>There are two main types of benefit structures, accelerated and standalone.</p>
    <p>Lump-sum benefits such as life, trauma and total permanent disablement are the covers affected by a
  standalone or accelerated structure.</p>
    <p>Under an accelerated benefit structure, the life cover benefit for that life assured will decrease by the
  amount paid under a claim on other lump-sum benefits.</p>
    <p>With a standalone benefit, when a claim is paid the payment does not affect any other lump-sum
  benefits.</p>
    <p>Benefits such as health insurance and income protection do not affect lump sum structures.</p>
    <h3>Premium structure</h3>
    <p>There are two main types of premium structures, level and rate for age.<br />
  With level cover, your premium stays the same for your chosen timeframe. This can be 10 years, to age
  65 or to age 80. Generally, your initial premium under this structure is higher, but the base premium
  remains constant throughout that timeframe. While the base premium remains constant, your premium will
  still increase for benefits that are indexed.</p>
    <p>With rate for age cover, your premium is based on the age you are now and increases each year with
  age.</p>
    <h3>Policy ownership</h3>
    <p>Policy ownership dictates to whom the proceeds go in the event of a claim. There are numerous options
  ranging from individual ownership and joint ownership to ownership options for business structures. The
  ownership may vary on each product.</p>
    <p>The policy owner can change the ownership of the policy to fit the insured's requirements as these
  change. The insurance company will pay all benefits to the policy owner, or to the insured's estate. If
  more than one person owns the policy then the insurance company will pay all benefits to the policy
  owners jointly or to the survivorls).</p>
