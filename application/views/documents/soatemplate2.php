         about yourself, including your health and medical history. This is called your duty of 
         disclosure. Your duty of disclosure extends to the date the contract of insurance is concluded 
         between yourself and the insurance provider. For example, you are required to tell the 
         insurance provider if you are diagnosed with a medical condition after the date of your 
         application but before you agree to any terms of cover they may offer. If they offer to cover 
         you, you will be insured on the basis of the information you have provided.

         <br>
      </li>

